import { _, DOM, CLASS_LIST } from "./utill.js";

DOM.cursor.addEventListener("mouseover", IN_CURSOR);
DOM.cursor.addEventListener("mouseout", OUT_CURSOR);

function IN_CURSOR() {
    _.removeClass(DOM.RIGHT_BUTTON,CLASS_LIST.CHANGE_RIGHT);
    _.addClass(DOM.RIGHT_BUTTON,CLASS_LIST.CAROUSEL_RIGHT_IMG);
    _.removeClass(DOM.LEFT_BUTTON,CLASS_LIST.CHANGE_LEFT);
    _.addClass(DOM.LEFT_BUTTON,CLASS_LIST.CAROUSEL_LEFT_IMG);
    DOM.RIGHT_BUTTON.addEventListener("mouseover", RIGHT_IN_BUTTON_CURSOR);
    DOM.LEFT_BUTTON.addEventListener("mouseover", LEFT_IN_BUTTON_CURSOR);
};

function OUT_CURSOR() {
    _.addClass(DOM.RIGHT_BUTTON,CLASS_LIST.CHANGE_RIGHT);
    _.removeClass(DOM.RIGHT_BUTTON,CLASS_LIST.CAROUSEL_RIGHT_IMG);
    _.addClass(DOM.LEFT_BUTTON,CLASS_LIST.CHANGE_LEFT);
    _.removeClass(DOM.LEFT_BUTTON,CLASS_LIST.CAROUSEL_LEFT_IMG);
    DOM.RIGHT_BUTTON.addEventListener("mouseout", OUT_BUTTON_CURSOR);
    DOM.LEFT_BUTTON.addEventListener("mouseout", OUT_BUTTON_CURSOR);
};

function RIGHT_IN_BUTTON_CURSOR() {
    _.removeClass(DOM.RIGHT_BUTTON,CLASS_LIST.CHANGE_RIGHT);
    _.removeClass(DOM.RIGHT_BUTTON,CLASS_LIST.CAROUSEL_RIGHT_IMG);
    _.addClass(DOM.RIGHT_BUTTON,CLASS_LIST.RIGHT_BUTTON_CURSOR);
};

function LEFT_IN_BUTTON_CURSOR() {
    _.removeClass(DOM.LEFT_BUTTON,CLASS_LIST.CHANGE_LEFT);
    _.removeClass(DOM.LEFT_BUTTON,CLASS_LIST.CAROUSEL_LEFT_IMG);
    _.addClass(DOM.LEFT_BUTTON,CLASS_LIST.LEFT_BUTTON_CURSOR);
}

function OUT_BUTTON_CURSOR() {
    _.removeClass(DOM.LEFT_BUTTON,CLASS_LIST.LEFT_BUTTON_CURSOR);
    _.removeClass(DOM.RIGHT_BUTTON,CLASS_LIST.RIGHT_BUTTON_CURSOR);
};